function test_ent(d::Int, C::Matrix)
    del, v, V = gen_pncp(d, d)
    poly = del * v + 10 * vec(V * V')

    ent = reshape(poly, d * d, d * d) * C
    display(eigvals(ent))

    return isposdef(ent), tr(ent)
end

