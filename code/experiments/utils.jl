function sep_state(n::Int, m::Int; n_terms::Int=2)
    d = n * m
    rho = zeros(d, d)

    for _ in 1:n_terms
        psiA = randn(n)
        psiB = randn(m)

        rhoA = psiA * psiA'
        rhoB = psiB * psiB'

        rho += kron(rhoA, rhoB)
    end

    return rho
end

function E(dim::Int, row::Int, col::Int)
    M = zeros(dim, dim)
    M[row, col] = 1
    return M
end

function choi(M::Matrix)
    d = Int(sqrt(size(M, 1)))
    C = zeros(d^2, d^2)

    for i in 1:d
        for j in 1:d
            E_ij = E(d, i, j)
            C = C + kron(E_ij, reshape(M * vec(E_ij), d, d))
        end
    end
    return C
end

